const TEXT_NODE_ORIGINALS = new WeakMap();
const TEXT_NODE_TRANSLATIONS = new WeakMap();
const ATTRIBUTE_ORIGINALS = new WeakMap();
const ATTRIBUTE_TRANSLATIONS = new WeakMap();
const TRANSLATABLE_ATTRIBUTES = ['placeholder', 'title', 'aria-label'];
const SKIP_TRANSLATION_SELECTOR = 'script, style, noscript, textarea, input[data-i18n-ignore], [data-i18n-ignore="true"]';

const escapeRegExp = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const preserveWhitespace = (original, translated) => {
  const leading = original.match(/^\s*/)?.[0] || '';
  const trailing = original.match(/\s*$/)?.[0] || '';
  return `${leading}${translated}${trailing}`;
};

const phraseEntries = Object.entries(translations.en)
  .map(([key, englishValue]) => {
    const khmerValue = translations.km?.[key];
    if (typeof englishValue !== 'string' || typeof khmerValue !== 'string') {
      return null;
    }

    const normalizedEnglish = englishValue.trim();
    const normalizedKhmer = khmerValue.trim();
    if (!normalizedEnglish || !normalizedKhmer || normalizedEnglish === normalizedKhmer) {
      return null;
    }

    const tokens = [];
    const patternSource = escapeRegExp(normalizedEnglish).replace(/\\\{\\\{\s*([\w]+)\s*\\\}\\\}/g, (_, token) => {
      tokens.push(token);
      return '(.+?)';
    });

    return {
      english: normalizedEnglish,
      khmer: normalizedKhmer,
      regex: new RegExp(`^${patternSource}$`),
      tokens
    };
  })
  .filter(Boolean)
  .sort((first, second) => second.english.length - first.english.length);

const translateStaticPhrase = (text) => {
  const normalized = text.trim();
  if (!normalized) return text;

  for (const entry of phraseEntries) {
    const match = normalized.match(entry.regex);
    if (!match) continue;

    let translated = entry.khmer;
    entry.tokens.forEach((token, index) => {
      translated = translated.replaceAll(`{{${token}}}`, match[index + 1]);
    });
    return preserveWhitespace(text, translated);
  }

  return text;
};

const shouldSkipElement = (element) =>
  !element ||
  !(element instanceof HTMLElement) ||
  element.matches(SKIP_TRANSLATION_SELECTOR) ||
  element.closest(SKIP_TRANSLATION_SELECTOR);

const processTextNode = (node, language) => {
  const currentValue = node.nodeValue || '';
  const previousTranslated = TEXT_NODE_TRANSLATIONS.get(node);

  if (language === 'km') {
    if (!TEXT_NODE_ORIGINALS.has(node) || (previousTranslated && currentValue !== previousTranslated)) {
      TEXT_NODE_ORIGINALS.set(node, currentValue);
    }

    const source = TEXT_NODE_ORIGINALS.get(node) ?? currentValue;
    const translated = translateStaticPhrase(source);
    TEXT_NODE_TRANSLATIONS.set(node, translated);

    if (translated !== currentValue) {
      node.nodeValue = translated;
    }
    return;
  }

  const original = TEXT_NODE_ORIGINALS.get(node);
  if (typeof original === 'string' && currentValue !== original) {
    node.nodeValue = original;
  }
  TEXT_NODE_TRANSLATIONS.delete(node);
};

const processElementAttributes = (element, language) => {
  TRANSLATABLE_ATTRIBUTES.forEach((attributeName) => {
    if (!element.hasAttribute(attributeName)) return;

    const currentValue = element.getAttribute(attributeName) || '';
    const originalAttributes = ATTRIBUTE_ORIGINALS.get(element) || {};
    const translatedAttributes = ATTRIBUTE_TRANSLATIONS.get(element) || {};

    if (language === 'km') {
      if (!(attributeName in originalAttributes) || (translatedAttributes[attributeName] && currentValue !== translatedAttributes[attributeName])) {
        originalAttributes[attributeName] = currentValue;
      }

      const source = originalAttributes[attributeName] ?? currentValue;
      const translated = translateStaticPhrase(source);
      translatedAttributes[attributeName] = translated;

      if (translated !== currentValue) {
        element.setAttribute(attributeName, translated);
      }

      ATTRIBUTE_ORIGINALS.set(element, originalAttributes);
      ATTRIBUTE_TRANSLATIONS.set(element, translatedAttributes);
      return;
    }

    if (attributeName in originalAttributes && currentValue !== originalAttributes[attributeName]) {
      element.setAttribute(attributeName, originalAttributes[attributeName]);
    }
  });

  if (element instanceof HTMLInputElement) {
    const isButtonLike = ['button', 'submit', 'reset'].includes(element.type);
    if (!isButtonLike || !element.value) return;

    const originalAttributes = ATTRIBUTE_ORIGINALS.get(element) || {};
    const translatedAttributes = ATTRIBUTE_TRANSLATIONS.get(element) || {};

    if (language === 'km') {
      if (!('value' in originalAttributes) || (translatedAttributes.value && element.value !== translatedAttributes.value)) {
        originalAttributes.value = element.value;
      }

      const translated = translateStaticPhrase(originalAttributes.value);
      translatedAttributes.value = translated;
      if (translated !== element.value) {
        element.value = translated;
      }

      ATTRIBUTE_ORIGINALS.set(element, originalAttributes);
      ATTRIBUTE_TRANSLATIONS.set(element, translatedAttributes);
      return;
    }

    if ('value' in originalAttributes && element.value !== originalAttributes.value) {
      element.value = originalAttributes.value;
    }
  }
};

const translateDomSubtree = (rootNode, language) => {
  if (typeof document === 'undefined' || !rootNode) return;

  if (rootNode.nodeType === Node.TEXT_NODE) {
    const parentElement = rootNode.parentElement;
    if (!shouldSkipElement(parentElement)) {
      processTextNode(rootNode, language);
    }
    return;
  }

  if (rootNode.nodeType !== Node.ELEMENT_NODE || shouldSkipElement(rootNode)) {
    return;
  }

  processElementAttributes(rootNode, language);

  const walker = document.createTreeWalker(rootNode, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
  let currentNode = walker.currentNode;

  while (currentNode) {
    if (currentNode.nodeType === Node.TEXT_NODE) {
      if (!shouldSkipElement(currentNode.parentElement)) {
        processTextNode(currentNode, language);
      }
    } else {
      processElementAttributes(currentNode, language);
    }

    currentNode = walker.nextNode();
  }
};

const LanguageContext = createContext(null);

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    try {
      return localStorage.getItem(LANGUAGE_STORAGE_KEY) || 'en';
    } catch {
      return 'en';
    }
  });

  const changeLanguage = useCallback((nextLanguage) => {
    const safe = nextLanguage === 'km' ? 'km' : 'en';
    if (safe === language) return;

    startTransition(() => {
      setLanguage(safe);
    });
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, safe);
    } catch {}
  }, [language]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  useEffect(() => {
    if (typeof document === 'undefined') return undefined;

    translateDomSubtree(document.body, language);

    const observer = new MutationObserver((mutations) => {
      mutations.forEach((mutation) => {
        if (mutation.type === 'characterData') {
          translateDomSubtree(mutation.target, language);
          return;
        }

        if (mutation.type === 'attributes') {
          translateDomSubtree(mutation.target, language);
          return;
        }

        mutation.addedNodes.forEach((node) => translateDomSubtree(node, language));
      });
    });

    observer.observe(document.body, {
      subtree: true,
      childList: true,
      characterData: true,
      attributes: true,
      attributeFilter: TRANSLATABLE_ATTRIBUTES
    });

    return () => observer.disconnect();
  }, [language]);

  const translate = useCallback((key, fallback, replacements = {}) => {
    const template = translations[language]?.[key] || fallback || key;
    return Object.entries(replacements).reduce(
      (result, [token, value]) => result.replaceAll(`{{${token}}}`, String(value)),
      template
    );
  }, [language]);

  const value = useMemo(
    () => ({
      language,
      setLanguage: changeLanguage,
      t: translate
    }),
    [changeLanguage, language, translate]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return context;
};
